DROP TABLE IF EXISTS TEACHER_EVALUATION;

CREATE TABLE TEACHER_EVALUATION(
 TEACHER_EVALUATION_ID VARCHAR PRIMARY KEY,
 TEACHER_ID VARCHAR,
 TOPIC_ID VARCHAR,
 SUBJECT_PRESENTATION DOUBLE PRECISION,
 DOCUMENTS_MADE_AVAILABLE DOUBLE PRECISION,
 FINAL_GRADE DOUBLE PRECISION,
 OTHER_COMMENTS VARCHAR,
 EVALUATION_CODE VARCHAR
);

ALTER TABLE TEACHER_EVALUATION ADD CONSTRAINT TEACHER_EVALUATION_TEACHER_FK FOREIGN KEY (TEACHER_ID) REFERENCES TEACHER (TEACHER_ID) ON DELETE CASCADE;
ALTER TABLE TEACHER_EVALUATION ADD CONSTRAINT TEACHER_EVALUATION_TOPIC_FK FOREIGN KEY (TOPIC_ID) REFERENCES TOPIC (TOPIC_ID) ON DELETE CASCADE;